                              ],
                            ),
                          ),
                        ),
                      ],
                    )
                  ],
                ),
              ),
            )
          : Center(
              child: CircularProgressIndicator(),
            ),
    );
  }

  Future<void> _scanImageFromNetwork() async {
    var response = await http.get(Uri.parse(
        'https://ca.slack-edge.com/TNM395DEX-U0294J8485Q-7204fdfd4fa9-512'));
    List<Face> faces =
        await _mlKitService.getFacesFromUint8(response.bodyBytes);
    print('FACES_ARE:$faces');
    profileImagePredictionData = _faceNetService
        .setCurrentPredictionFromUint8List(response.bodyBytes, faces[0]);
    print('PREDICTION_LIST:$profileImagePredictionData');
  }
}
